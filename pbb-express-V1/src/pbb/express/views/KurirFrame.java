    private void listKurirDaftarPesananValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listKurirDaftarPesananValueChanged
        // TODO add your handling code here:
        Pesanan pesanan = listKurirDaftarPesanan.getSelectedValue();
        
        if (pesanan != null) {
            labelKurirNamaPenerima.setText("Nama Penerima: " + pesanan.getPenerima().getNamaPenerima());
            labelKurirNamaPengirim.setText("Nama Pengirim: " + pesanan.getPengirim().getNamaPengirim());
            
            labelKurirNomorPenerima.setText("Nomor Penerima: " + pesanan.getPenerima().getNomor());
            labelKurirNomorPengirim.setText("Nomor Pengirim: " + pesanan.getPengirim().getNomor());
            
            labelKurirNomorResi.setText("Nomor Resi: " + pesanan.getNomorResi());
        }
    }//GEN-LAST:event_listKurirDaftarPesananValueChanged

