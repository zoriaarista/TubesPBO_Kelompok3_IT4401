    private void btnKurirCekAlamatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKurirCekAlamatActionPerformed
        // TODO add your handling code here:
        Pesanan pesanan = listKurirDaftarPesanan.getSelectedValue();
        
        if (pesanan != null) {
            JOptionPane.showMessageDialog(this, "Alamat Tujuan: " + ((Kurir) Driver.getLoggedAccount()).cekAlamat(pesanan));
        }
    }//GEN-LAST:event_btnKurirCekAlamatActionPerformed

    private void btnKurirCekMetodePembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKurirCekMetodePembayaranActionPerformed
        // TODO add your handling code here:
        Pesanan pesanan = listKurirDaftarPesanan.getSelectedValue();
        
        if (pesanan != null) {
            JOptionPane.showMessageDialog(this, ((Kurir) Driver.getLoggedAccount()).cekMetodePembayaran(pesanan));
        }
    }//GEN-LAST:event_btnKurirCekMetodePembayaranActionPerformed

    private void btnKurirCekBarangSampaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKurirCekBarangSampaiActionPerformed
        // TODO add your handling code here:
        Pesanan pesanan = listKurirDaftarPesanan.getSelectedValue();
        
        if (pesanan != null) {
            ((Kurir) Driver.getLoggedAccount()).cekBarangSampai(pesanan);
            JOptionPane.showMessageDialog(this, "Barang sudah diterima Penerima");
        }
    }//GEN-LAST:event_btnKurirCekBarangSampaiActionPerformed

